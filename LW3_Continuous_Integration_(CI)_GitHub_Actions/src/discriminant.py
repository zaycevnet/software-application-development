def discriminant(a, b, c):
    """Вычисляет дискриминант квадратного уравнения ax^2 + bx + c = 0"""
    return b ** 2 - 4 * a * c


if __name__ == "__main__":
    print("--- Решение квадратного уравнения (ax^2 + bx + c = 0) ---")
    try:
        # Пытаемся получить ввод от пользователя
        a = float(input("Введите коэффициент a: "))
        b = float(input("Введите коэффициент b: "))
        c = float(input("Введите коэффициент c: "))

        if a == 0:
            print("Ошибка: Коэффициент 'a' не может быть равен нулю для квадратного уравнения.")
        else:
            d = discriminant(a, b, c)
            print(f"Дискриминант D равен: {d}")

    except ValueError:
        # Ловим ошибку, если ввод нельзя преобразовать во float
        print("❌ Ошибка ввода! Пожалуйста, вводите только числа.")
    except Exception as e:
        # Ловим любые другие непредвиденные ошибки
        print(f"❌ Произошла непредвиденная ошибка: {e}")
    finally:
        print("--- Программа завершена ---")